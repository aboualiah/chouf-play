const LANG_KEY = "chouf_language";

export function getSavedLang(): Lang {
  try {
    const saved = localStorage.getItem(LANG_KEY);
    if (saved && translations[saved as Lang]) return saved as Lang;
  } catch {}
  return "fr";
}

export function saveLang(lang: Lang) {
  localStorage.setItem(LANG_KEY, lang);
  // Apply RTL
  const isRtl = LANGUAGES.find(l => l.code === lang)?.rtl;
  document.documentElement.dir = isRtl ? "rtl" : "ltr";
}

export function t(key: string, lang?: Lang): string {
  const l = lang || getSavedLang();
  return translations[l]?.[key] || translations.fr[key] || key;
}

// React hook
export interface I18nContextValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
}

export const I18nContext = createContext<I18nContextValue>({
  lang: "fr",
  setLang: () => {},
  t: (key) => translations.fr[key] || key,
});

export function useI18n() {
  return useContext(I18nContext);
}
